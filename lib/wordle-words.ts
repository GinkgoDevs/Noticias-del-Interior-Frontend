// Función para obtener la palabra del día
export function getDailyWord(): string {
  const today = new Date()
  today.setHours(0, 0, 0, 0) // Normalizar a medianoche
  const daysSinceEpoch = Math.floor(today.getTime() / (1000 * 60 * 60 * 24))
  const wordIndex = daysSinceEpoch % WORDLE_WORDS.length
  return WORDLE_WORDS[wordIndex].toUpperCase()
}

// Función para validar si una palabra existe en la lista
export function isValidWord(word: string): boolean {
  return WORDLE_WORDS.includes(word.toLowerCase())
}
